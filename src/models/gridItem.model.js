class GridItem {
  constructor(
    type,
    id,
    text,
    subText,
    color,
    imgUrl,
    linkUrl,
    textAtBottom,
    textUpperCase
  ) {
    this.type = type
    this.id = id
    this.text = text
    this.subText = subText
    this.color = color
    this.imgUrl = imgUrl
    this.linkUrl = linkUrl
    this.textAtBottom = textAtBottom
    this.textUpperCase = textUpperCase
  }
}
export default GridItem
